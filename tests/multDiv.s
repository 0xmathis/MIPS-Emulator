SLL $2, $1, 16  # $2 <- 0xFFFF0000 (négatif)
ADDI $3, $zero, 123456789  # $3 <- 123456789
MULT $2, $3  # HI <- 0xFFFFF8A4 (négatif); LO <- 0x32EB0000 (positif)

MFHI $4  # $4 <- 0xFFFFF8A4
MFLO $5  # $5 <- 0x32EB0000

AND $6, $1, $4  # $6 <- 0xFFFFF8A4 & 0xFFFF = 0xF8A4
SRL $7, $5, 16  # $7 <- 0x32EB0000 >> 16 = 0x32EB

DIV $6, $7  # HI <- 11512; LO <- 4

MFHI $8
MFLO $9


